#include "libft.h"

int	ft_atoi_pointer(char *str, int *p)
{
	int			i;
	int			n;
	long long	x;

	x = 0;
	n = 1;
	i = 0;
	while (str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		n = -1;
		i++;
	}
	while (str[i] != ' ' && str[i] != '\0')
	{
		x = x * 10 + (str[i] - '0');
		i++;
	}
	x = x * n;
	if (x > 2147483647 || x < -2147483648)
		return (-1);
	*p = x;
	return (0);
}
